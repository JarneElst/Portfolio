                    Just one more beer we tought. We actually went to bed a couple of hours, and a couple of beers,
                    later.

                    Day two of Brussels started with a simple but good breakfast. Once arrived at Fosdem Vincent and I
                    immediately started with checking out all the different stands. We asked for information, took
                    flyers with us and Vincent collected some stickers to cover up that he is using a windows laptop. A
                    couple of hours and a talk about Tor later, we decided it was time to go home again.
