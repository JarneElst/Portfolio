                    monitoring.

                    Na de installatieprocedure en de gebruikelijke Windows update, was het tijd om deze server aan te
                    sluiten aan het internet. Poort één gaf geen signaal, en al snel bleek dat ook poort twee geen
                    signaal doorgaf van de ethernetkabel. Met een ethernet naar usb adapter konden we ons op dat moment
                    even behelpen, maar dit was slechts een tijdelijke oplossing. Ik ben op zoek gegaan naar drivers om
                    de ethernetpoorten aan te sturen en na, tevergeefs, verschillende drivers te hebben geïnstalleerd
                    van de Intel website, begon de moed me in de schoenen te zinken.

                    Nadat ook Didier enkele uren had meegezocht naar een oplossing, bleek de klus moeilijker te klaren
                    dan aanvankelijk gedacht. Ik ben dan gestart met het volgen van een stappenplan dat we hadden
                    gevonden om Github. Zo zouden met behulp van powershell en het aanpassen van een .inf file de
                    ethernet adapters aan de praat moeten krijgen. Uiteraard, tevergeefs. Didier was er die dag niet, en
                    de installatie was wederom mislukt. De volgende dag bracht ik Didier op de hoogte, en nadat hij
                    hetzelfde stappenplan had gevolgd was het wel gelukt. Omdat we nu wisten waar de fout lag, heeft
                    Didier me de hele server laten verwijderen om vervolgens opnieuw te beginnen aan de installatie.
                    Gelukkig ging deze herinstallatie heel wat vlotter dan de oorspronkelijke, enkele uren in plaats van
                    enkele dagen later was de server up and running.

                    De installatie van de Windows 10 virtuele machine die op de server komt, moest slechts een kleine
                    voetnoot worden in de hele installatie. Een procedure volgen die voorhanden was, dat kon niet
                    moeilijk zijn. Elke stap die ik gezet had, resulteerde in foutmeldingen en veel opzoekwerk. Zo heb
